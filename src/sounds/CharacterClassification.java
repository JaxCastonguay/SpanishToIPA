package sounds;

import java.util.ArrayList;
import java.util.List;

public class CharacterClassification {
	public final List<Character> vowels;
	
	CharacterClassification(){
		this.vowels = new ArrayList<Character>();
		this.vowels.add('a');
		this.vowels.add('e');
		this.vowels.add('i');
		this.vowels.add('o');
		this.vowels.add('u');
		
	}
}
